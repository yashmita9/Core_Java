package In.com.corejava;

public class SwapWithVar {
	public static void main(String[] args) {
		
		int a = 10;
		int b = 50;
		int c = a;
		System.out.println(" Print value of a before swaping : " +a);
		System.out.println(" Print value of b before swaping : " +b);
		a = b;
		b = c;
		System.out.println(" Print value of a after swaping : " +a);
		
		System.out.println(" print value of b after swaping : " +b);
		
	}

}
